<h2>Free and open source</h2>
<p>
  Even if you're not an open source junkie it's important to understand why being free and open source is so important.
  It means anyone can use the software any way they like.
  Much of the greatest software available today is open source or based off open source software.
</p>
<p>
  Open sourcing OpenPhoto means that the community is in control of their own destination.
  Sub communities can even embark on a new path to a different destination.
  Imagine a professional photographer taking OpenPhoto and catering it to the needs of their colleague.
  Or a group of mothers using OpenPhoto to tell the stories of their children as they grow up.
</p>
<p>
  We don't know where OpenPhoto will head but we're excited that you'll be making the calls.
</p>
