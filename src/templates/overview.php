<h2>The OpenPhoto Story</h2>
<div class="row">
  <div class="span7">
    <p>
      <img src="/img/github-100.png" align="left" class="why-icons">The inception of OpenPhoto was a desire to liberate our photos and take back control.
      Like you, our photos are the most valuable digital files we have.
      Also like you, we've used Flickr, Picasa and Smugmug and wound up with our photos scattered across numerous sites on the web.
    </p>
    <p>
      We knew there had to be a better way to take, share and archive photos while retaining ownership.
      It didn't seem like anyone else was going to build it so we took it upon ourselves to solve this problem for everyone.
      The project has been <a href="https://github.com/photo/frontend">open source on Github</a> since the first line of code was written because like your photos, OpenPhoto doesn't belong to us.
    </p>
  </div>
  <div class="span7 offset1">
    <p>
      <img src="/img/kickstarter-100.png" align="left" class="why-icons">The OpenPhoto Project was <a href="http://www.kickstarter.com/projects/jmathai/openphoto-a-photo-service-for-your-s3-or-dropbox-a">launched on Kickstarter</a> and quickly reached its funding goal.
      More important than the funding was when we realized there were thousands of people waiting for the same solution we were.
    </p>
    <p>
      We quickly got to work and launched the first version of OpenPhoto just 3 months after development.
      Since then the community of contributors has grown and we've been releasing features on a weekly basis.
      It's been an exceptional experience to talk to members of the community and we've made some great friends who share the same passion of photo liberation as we do.
    </p>
  </div>
</div>

