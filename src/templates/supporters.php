<h1>The original founders of The OpenPhoto Project</h1>

<p>
  OpenPhoto was originally <a href="http://www.kickstarter.com/projects/jmathai/openphoto-a-photo-service-for-your-s3-or-dropbox-a" rel="external" target="_blank">started as a Kickstarter project</a>.
  Over 400 people pledged a total of over $25,000.
  Without them the project wouldn't exist.
</p>

